package de.cargame.controller.entity;

/**
 * Represents the different modes of gameplay available in the game.
 * This enumeration defines the following options:
 * <p>
 * - SINGLEPLAYER: A mode where a single player participates in the game.
 * - MULTIPLAYER: A mode where two players participate and interact within the game.
 * - NOT_SET: A default or undefined state for the game mode, which may be used as an initial value
 * or to indicate that the game mode has not been configured or selected.
 */
public enum GameMode {


    SINGLEPLAYER,
    MULTIPLAYER,
    NOT_SET
}
